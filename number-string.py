## 數字運算

x = 7/3 # 一個 / 除到小數
# print(x)

x = 7//3 # 一個 // 只保留整數位
# print(x)

x = 7%3 #取餘數
# print(x)

x = 1
x *= 2 # 等於 x = x*2 , 同樣有/=、+=、-=
# print(x)

